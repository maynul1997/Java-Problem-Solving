public class TypeCasting {
    static void main() {

        int num1, num2, result;

        num1 = 40;
        num2 = 30;

        result = num1 + num2;
        System.out.println("Sum:"+result);

        result = num1 - num2;
        System.out.println("Sub:"+result);

        result = num1 * num2;
        System.out.println("Mul:"+result);

    }
}
