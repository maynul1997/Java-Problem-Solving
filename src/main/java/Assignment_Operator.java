public class Assignment_Operator {
   public static void main(String[]Args) {

        int x = 10;
        int y = 20;

        x += y; // x = x + y = 30
       System.out.println("x = "+x);

       x -= y; // x = x - y = 10;
       System.out.println("x = "+x);

       x *= y; // x = x * y = 200;
       System.out.println("x = "+x);

       x /= y; // x = x / y = 10;
       System.out.println("x = "+x);

       x %= y; // x = x % y = 30;
       System.out.println("x = "+x);


    }
}
