public class Assignment_Operator {
   public static void main(String[]Args) {

        int x = 10;
        int y = 20;

        x += y; // x = x + y; x = 10 + 20;
       System.out.println("x = "+x);

       x -= y;
       System.out.println("x = "+x);

       x *= y;
       System.out.println("x = "+x);



    }
}
