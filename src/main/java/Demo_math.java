public class Demo_math {
    static void main() {
        int a = 20;
        int b = 80;

        int max = Math.max(a,b);
        System.out.println("Maximum value print = "+max);

        int minimum = Math.min(a,b);
        System.out.println("Minimun value Print= "+minimum);

        int absolute = Math.abs(a);
        System.out.println("Absolute value print= "+absolute);

        int round = Math.round(5.5f);
        System.out.println("The round number of 5.5 = "+round);





    }
}
