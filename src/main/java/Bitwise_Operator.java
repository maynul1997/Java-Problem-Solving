public class Bitwise_Operator {
    static void main() {

        int a= 32;
        int b = 12;

        int c;

        c = a & b;
        System.out.println("a & b = "+c);

        c = a | b;
        System.out.println("a | b = "+c);

        c = a ^ b;
        System.out.println("a ^ b= "+c);


    }
}
